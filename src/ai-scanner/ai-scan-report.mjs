// AI scan report — renders AiScanResult to Markdown / terminal.

const ICON = {
  'exposed': '⚠️',
  'licensed': '💸',
  'action-needed': '❓',
  'safe': '✅',
  'not-applicable': '—'
};

const DIFFICULTY_LABEL = {
  'none': 'none',
  'easy': 'easy (toggle)',
  'medium': 'medium (form)',
  'hard': 'hard (email)',
  'impossible': 'no opt-out'
};

export function renderAiScanReport(scanResult) {
  const { exposureScore, riskLevel, summary, quickWins, hardItems, licensedItems, safePlatforms, exposures } = scanResult;
  const lines = [];

  lines.push('# AI Training Exposure Report');
  lines.push('');
  lines.push(`**Scan ID**: ${scanResult.scanId}`);
  lines.push(`**Scanned**: ${scanResult.scannedAt}`);
  lines.push('');

  const fillBar = Math.round(exposureScore / 5);
  const bar = '█'.repeat(fillBar) + '░'.repeat(20 - fillBar);
  lines.push(`## AI Exposure Score: ${exposureScore}/100 (${riskLevel.toUpperCase()} RISK)`);
  lines.push('');
  lines.push('```');
  lines.push(`[${bar}] ${exposureScore}/100`);
  lines.push('```');
  lines.push('');

  // Summary
  lines.push('## Summary');
  lines.push('');
  lines.push(`| Metric | Count |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Platforms you use | ${summary.totalPlatformsChecked} |`);
  lines.push(`| ⚠️  Exposed (opted-in by default) | **${summary.exposed}** |`);
  lines.push(`| 💸 Licensed (sold to AI already) | ${summary.licensed} |`);
  lines.push(`| ❓ Need to check settings | ${summary.actionNeeded} |`);
  lines.push(`| ✅ Safe (opted-out by default) | ${summary.safe} |`);
  lines.push('');

  // Quick wins
  if (quickWins.length > 0) {
    lines.push(`## ⚡ Quick wins — ${quickWins.length} easy opt-outs`);
    lines.push('');
    for (const e of quickWins) {
      const seconds = e.estimatedSeconds ? ` (~${e.estimatedSeconds}s)` : '';
      lines.push(`- **${e.displayName}**${seconds} — ${DIFFICULTY_LABEL[e.optOutDifficulty]}`);
      if (e.optOutUrl) lines.push(`  → ${e.optOutUrl}`);
      if (e.notes) lines.push(`  _${truncate(e.notes, 180)}_`);
    }
    lines.push('');
  }

  // Licensed
  if (licensedItems.length > 0) {
    lines.push(`## 💸 Already licensed to AI — ${licensedItems.length} platform(s)`);
    lines.push('');
    lines.push('These platforms have **already sold or licensed** your content to AI companies. Opting out now affects future training only — past training data already shipped.');
    lines.push('');
    for (const e of licensedItems) {
      lines.push(`- **${e.displayName}** → ${e.aiModels.join(', ')}`);
      if (e.notes) lines.push(`  _${truncate(e.notes, 180)}_`);
    }
    lines.push('');
  }

  // Hard items
  if (hardItems.length > 0) {
    lines.push(`## 🧱 Hard opt-outs — ${hardItems.length} platform(s)`);
    lines.push('');
    lines.push('These require email/form submissions, GDPR/CCPA requests, or have no user-facing opt-out.');
    lines.push('');
    for (const e of hardItems) {
      lines.push(`- **${e.displayName}** — ${DIFFICULTY_LABEL[e.optOutDifficulty]}`);
      if (e.optOutUrl) lines.push(`  → ${e.optOutUrl}`);
      if (e.notes) lines.push(`  _${truncate(e.notes, 180)}_`);
    }
    lines.push('');
  }

  // Safe
  if (safePlatforms.length > 0) {
    lines.push(`## ✅ Safe by default — ${safePlatforms.length} platform(s)`);
    lines.push('');
    lines.push(safePlatforms.map(e => `- **${e.displayName}** — ${e.notes || 'Opted-out by default'}`).join('\n'));
    lines.push('');
  }

  lines.push('---');
  lines.push('*Generated by Vanish AI Scanner · data verified 2026-04*');

  return lines.join('\n');
}

export function renderAiScanBanner(scanResult, options = {}) {
  const color = options.color !== false;
  const { exposureScore, riskLevel, summary } = scanResult;
  const RISK_ANSI = {
    critical: '\x1b[31m', high: '\x1b[33m', moderate: '\x1b[33m', low: '\x1b[32m'
  };
  const c = color ? (RISK_ANSI[riskLevel] || '') : '';
  const b = color ? '\x1b[1m' : '';
  const r = color ? '\x1b[0m' : '';
  const d = color ? '\x1b[2m' : '';

  const fillBar = Math.round(exposureScore / 5);
  const bar = '█'.repeat(fillBar) + '░'.repeat(20 - fillBar);

  const WIDTH = 52;
  const INNER = WIDTH - 6;
  const pad = (s) => s + ' '.repeat(Math.max(0, INNER - visibleLen(s)));
  const center = (s) => {
    const vl = visibleLen(s);
    const left = Math.floor((INNER - vl) / 2);
    const right = INNER - vl - left;
    return ' '.repeat(Math.max(0, left)) + s + ' '.repeat(Math.max(0, right));
  };
  const line = (s) => `│  ${pad(s)}  │`;
  const blank = `│${' '.repeat(WIDTH - 2)}│`;

  const lines = [];
  lines.push(`┌${'─'.repeat(WIDTH - 2)}┐`);
  lines.push(blank);
  lines.push(line(`${b}🤖 AI Training Exposure${r}`));
  lines.push(blank);
  lines.push(line(`${c}${b}${center(`${exposureScore} / 100`)}${r}`));
  lines.push(line(`${c}${center(`[${bar}]`)}${r}`));
  lines.push(line(`${c}${b}${center(riskLevel.toUpperCase() + ' RISK')}${r}`));
  lines.push(blank);
  lines.push(line(`${summary.exposed} of ${summary.totalPlatformsChecked} likely feed AI models`));
  if (summary.licensed > 0) lines.push(line(`${d}${summary.licensed} already licensed to AI companies${r}`));
  lines.push(blank);
  lines.push(`└${'─'.repeat(WIDTH - 2)}┘`);

  return lines.join('\n');
}

// Helpers
function visibleLen(s) {
  // eslint-disable-next-line no-control-regex
  return s.replace(/\x1b\[[0-9;]*m/g, '').length;
}
function truncate(s, n) {
  return s.length > n ? s.slice(0, n - 1) + '…' : s;
}
